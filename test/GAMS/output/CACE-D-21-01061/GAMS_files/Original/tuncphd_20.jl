using JuMP
model = Model()
@variable(model, -8 <= x1 <= 10)
@variable(model, 0 <= x2 <= 10)
@variable(model, obj)
@constraint(model, ob, (x1)^(4) - 14*(x1)^(2) + 24*x1 - (x2)^(2) == obj)
@constraint(model, c1, -x1+x2-8 <= 0)
@constraint(model, c2, x2-x1*x1-2*x1+2 <= 0)
@objective(model, Min, obj)
optimize!(model)
