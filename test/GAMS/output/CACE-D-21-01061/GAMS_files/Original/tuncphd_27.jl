using JuMP
function build_model()
    model = Model()
    @variable(model, 0 <= x1 <= 300)
    @variable(model, 0 <= x2 <= 300)
    @variable(model, 0 <= x3 <= 100)
    @variable(model, 0 <= x4 <= 200)
    @variable(model, 0 <= x5 <= 100)
    @variable(model, 1 <= x6 <= 3)
    @variable(model, 0 <= x7 <= 100)
    @variable(model, 0 <= x8 <= 200)
    @variable(model, 0 <= x9 <= 200)
    @variable(model, obj)
    @constraint(model, ob, -9*x5-15*x9+6*x1+16*x2+10*x7+10*x8 == obj)
    @constraint(model, c1, x1+x2 == x3+x4)
    @constraint(model, c2, x3+x7 == x5)
    @constraint(model, c3, x4+x8 == x9)
    @constraint(model, c4, x3*x6+2*x7 <= 2.5*x5)
    @constraint(model, c5, x4*x6+2*x8 <= 1.5*x9)
    @constraint(model, c6, 3*x1+x2-x3*x6-x4*x6 == 0)
    @objective(model, Min, obj)
    optimize!(model)
    return model
end
