using JuMP
function build_model(; _kwargs...)
    model = Model()
    @variable(model, objvar)
    @variable(model, 4.920281031555946 <= X0 <= 126.4021517244466)
    @variable(model, 1 <= X5 <= 5)
    @variable(model, 1 <= X6 <= 3.741159586697598)
    @variable(model, 1 <= X7 <= 6.654360313360264)
    @variable(model, 1.920281031555946 <= X8 <= 12.89802340802677)
    @variable(model, 1 <= X9 <= 12.89802340802677)
    @variable(model, -0 <= X1 <= 216.2678196119469)
    @variable(model, 1.920281031555946 <= X10 <= 18.70579793348799)
    @variable(model, 0 <= X2 <= 0)
    @variable(model, 1.920281031555946 <= X4 <= 5)
    @variable(model, 1 <= X3 <= 2.579604681605355)
    @constraint(model, E1, - X0 + X5 + X6*X7 + X6*X8 + X6*X9 == 0)
    @constraint(model, E2, - X1 + X9*X10 == 25)
    @constraint(model, E3, - X2 + X7 + SQR(X4) + SQR(X5) + SQR(X6) == 40)
    @constraint(model, E4, - X9 + X3*X5 == 0)
    @constraint(model, E5, - X8 + X3*X4 == 0)
    @constraint(model, E6, X4*X9 - X5*X8 == 0)
    @constraint(model, E7, - X7 + SQR(X3) == 0)
    @constraint(model, E8, X3*X8 - X4*X7 == 0)
    @constraint(model, E9, X3*X9 - X5*X7 == 0)
    @constraint(model, E10, - X10 + X4*X6 == 0)
    @constraint(model, E11, - X3*X10 + X6*X8 == 0)
    @constraint(model, E12, - 2*X3 + SQR(X3) >= -1)
    @constraint(model, E13, 2*X3 + SQR(X3) >= -1)
    @constraint(model, E14, - 2*X4 + SQR(X4) >= -1)
    @constraint(model, E15, 2*X4 + SQR(X4) >= -1)
    @constraint(model, E16, - 2*X5 + SQR(X5) >= -1)
    @constraint(model, E17, 2*X5 + SQR(X5) >= -1)
    @constraint(model, E18, - 2*X6 + SQR(X6) >= -1)
    @constraint(model, E19, 2*X6 + SQR(X6) >= -1)
    @constraint(model, E20, SQR(X3) - 2*X3*X4 + SQR(X4) >= 0)
    @constraint(model, E21, SQR(X3) + 2*X3*X4 + SQR(X4) >= 0)
    @constraint(model, E22, SQR(X3) - 2*X3*X5 + SQR(X5) >= 0)
    @constraint(model, E23, SQR(X3) + 2*X3*X5 + SQR(X5) >= 0)
    @constraint(model, E24, SQR(X4) - 2*X4*X6 + SQR(X6) >= 0)
    @constraint(model, E25, SQR(X4) + 2*X4*X6 + SQR(X6) >= 0)
    @constraint(model, E26, objvar == X0)
    @objective(model, Min, objvar)
    optimize!(model)
    return model
end
