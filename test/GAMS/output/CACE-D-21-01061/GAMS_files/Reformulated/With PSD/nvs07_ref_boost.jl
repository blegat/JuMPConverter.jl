using JuMP
function build_model()
    model = Model()
    @variable(model, objvar)
    @variable(model, 1.34 <= X2 <= 5.34)
    @variable(model, 4 <= X0 <= 10)
    @variable(model, 2 <= X1 <= 14)
    @variable(model, 0 <= X8 <= 0)
    @variable(model, Y5, Bin)
    @variable(model, Y4, Bin)
    @variable(model, 4 <= Y3 <= 8, Int)
    @constraint(model, E1, - X2 + Y3 - Y5 == 2.66)
    @constraint(model, E2, - X0 + Y3 + 5*Y5 + 2*SQR(Y4) == 0)
    @constraint(model, E3, - X1 + 3*Y3 + 5*Y5 + Y4*X8 == 10)
    @constraint(model, E4, - X8 + SQR(Y5) == 0)
    @constraint(model, E5, - 2*Y4 + SQR(Y4) >= -1)
    @constraint(model, E6, 2*Y4 + SQR(Y4) >= -1)
    @constraint(model, E7, - 2*Y5 + SQR(Y5) >= -1)
    @constraint(model, E8, 2*Y5 + SQR(Y5) >= -1)
    @constraint(model, E9, objvar == X0)
    @objective(model, Min, objvar)
    return model
end
