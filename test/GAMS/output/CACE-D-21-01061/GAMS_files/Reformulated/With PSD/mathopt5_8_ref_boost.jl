using JuMP
function build_model()
    model = Model()
    @variable(model, objvar)
    @variable(model, -37.10077927035776 <= X0 <= 45.06749245422674)
    @variable(model, -1.901841218326561 <= X1 <= 2.397699104653345)
    @variable(model, 0 <= X2 <= 5.748960996455454)
    @variable(model, -6.878959724202619 <= X3 <= 13.78427863388825)
    @constraint(model, E1, - X0 - X1 + 2*X2 - 1.05*X1*X3 + 0.166667*SQR(X3) == 0)
    @constraint(model, E2, - X2 + SQR(X1) == 0)
    @constraint(model, E3, - X3 + X1*X2 == 0)
    @constraint(model, E4, - 2*X1 + SQR(X1) >= -1)
    @constraint(model, E5, 2*X1 + SQR(X1) >= -1)
    @constraint(model, E6, - 2*X3 + SQR(X3) >= -1)
    @constraint(model, E7, 2*X3 + SQR(X3) >= -1)
    @constraint(model, E8, SQR(X1) - 2*X1*X3 + SQR(X3) >= 0)
    @constraint(model, E9, SQR(X1) + 2*X1*X3 + SQR(X3) >= 0)
    @constraint(model, E10, objvar == X0)
    @objective(model, Min, objvar)
    return model
end
