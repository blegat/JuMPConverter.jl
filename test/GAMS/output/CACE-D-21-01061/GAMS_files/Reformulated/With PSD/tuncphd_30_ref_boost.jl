    @constraint(model, E1, - X0 + 0.0187*X17 + 0.0204*X18 + 0.0204*X19 + 0.0187*X21 + 0.029359*X4*X13 + 0.068609*X5*X23 + 0.0204*X6*X9 + 0.0607*X6*X22 + 0.0607*X15*X18 + 0.0607*X15*X19 + 0.0437*X16*X17 + 0.0437*X16*X21 == 0)
