@variable(model, 0 <= X2 <= 0)
@variable(model, 0 <= X9 <= 100)
@variable(model, 0 <= X11 <= 100)
@variable(model, 0 <= X13 <= 100)
@variable(model, -3900 <= X0 <= 6317.46085240635)
@variable(model, 0 <= X7 <= 171.4285886714286)
@variable(model, 0 <= X8 <= 205.5555762111111)
@variable(model, 0 <= X14 <= 100.0000101)
