using JuMP
function build_model()
    model = Model()
    @variable(model, objvar)
    @variable(model, 0 <= X2 <= 0)
    @variable(model, 0 <= X5 <= 3.486784401000014e-14)
    @variable(model, -0 <= X6 <= 3.486784401000014e-15)
    @variable(model, -1 <= X3 <= -0.9999999999998721)
    @variable(model, -2.000000000000116 <= X4 <= -1.999999999999988)
    @variable(model, 0 <= X0 <= 2.701703435346006e-28)
    @variable(model, 0 <= X11 <= 1.215766545905703e-27)
    @variable(model, 0 <= X1 <= 0)
    @constraint(model, E1, - X2 + X5 - 3*X6 == -0)
    @constraint(model, E2, - X3 + X5 + X6 == 1)
    @constraint(model, E3, - X4 - X5 + X6 == 2)
    @constraint(model, E4, - X0 + 2*SQR(X6) - 16*X6*X11 + 40*SQR(X11) == 0)
    @constraint(model, E5, - X1 + X5 - 10*X6 - X5*X6 == 0)
    @constraint(model, E6, - X11 + SQR(X5) == 0)
    @constraint(model, E7, objvar == X0)
    @objective(model, Min, objvar)
    return model
end
