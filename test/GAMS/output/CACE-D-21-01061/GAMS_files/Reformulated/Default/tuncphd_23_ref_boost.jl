using JuMP
function build_model(; _kwargs...)
    model = Model()
    @variable(model, objvar)
    @variable(model, 4.920281031555946 <= X0 <= 126.4021517244466)
    @variable(model, 1 <= X5 <= 5)
    @variable(model, 1 <= X6 <= 3.741159586697598)
    @variable(model, 1 <= X7 <= 6.654360313360264)
    @variable(model, 1.920281031555946 <= X8 <= 12.89802340802677)
    @variable(model, 1 <= X9 <= 12.89802340802677)
    @variable(model, -0 <= X1 <= 216.2678196119469)
    @variable(model, 1.920281031555946 <= X10 <= 18.70579793348799)
    @variable(model, 0 <= X2 <= 0)
    @variable(model, 1.920281031555946 <= X4 <= 5)
    @variable(model, 1 <= X3 <= 2.579604681605355)
    @constraint(model, E1, - X0 + X5 + X6*X7 + X6*X8 + X6*X9 == 0)
    @constraint(model, E2, - X1 + X9*X10 == 25)
    @constraint(model, E3, - X2 + X7 + SQR(X4) + SQR(X5) + SQR(X6) == 40)
    @constraint(model, E4, - X9 + X3*X5 == 0)
    @constraint(model, E5, - X8 + X3*X4 == 0)
    @constraint(model, E6, - X7 + SQR(X3) == 0)
    @constraint(model, E7, - X10 + X4*X6 == 0)
    @constraint(model, E8, objvar == X0)
    @objective(model, Min, objvar)
    optimize!(model)
    return model
end
