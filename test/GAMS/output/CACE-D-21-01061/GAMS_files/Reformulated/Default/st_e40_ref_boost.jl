using JuMP
function build_model()
    model = Model()
    @variable(model, objvar)
    @variable(model, 16.4142135 <= X0 <= 52.970562)
    @variable(model, -51.26611440000001 <= X2 <= 0)
    @variable(model, 9.692159999999999 <= X12 <= 60.95827440000001)
    @variable(model, -54.96027440000001 <= X3 <= 0)
    @variable(model, -82.45827440000001 <= X4 <= -13.69216)
    @variable(model, -56.95827440000001 <= X5 <= 0)
    @variable(model, 0 <= X1 <= 0)
    @variable(model, 0 <= X6 <= 0)
    @variable(model, 25 <= X29 <= 144)
    @variable(model, 125 <= X32 <= 1728)
    @variable(model, 15625 <= X35 <= 2985984)
    @variable(model, 0 <= X7 <= 0)
    @variable(model, 25 <= X30 <= 144)
    @variable(model, 125 <= X33 <= 1728)
    @variable(model, 15625 <= X36 <= 2985984)
    @variable(model, 0 <= X8 <= 0)
    @variable(model, 1 <= X31 <= 144)
    @variable(model, 1 <= X34 <= 1728)
    @variable(model, 1 <= X37 <= 2985984)
    @variable(model, 5 <= Y9 <= 12, Int)
    @variable(model, 5 <= Y10 <= 12, Int)
    @variable(model, 1 <= Y11 <= 12, Int)
    @constraint(model, E1, - X0 + 2*Y9 + Y10 + 1.41421*Y11 == -0)
    @constraint(model, E2, - X2 - X12 + 1.7317*Y10 + 1.03366*Y11 == -0)
    @constraint(model, E3, - X3 - X12 + 0.634*Y9 + 2.828*Y11 == -0)
    @constraint(model, E4, - X4 - X12 + 0.5*Y9 - 2*Y10 == -0)
    @constraint(model, E5, - X5 - X12 - 0.5*Y9 + 2*Y10 == -0)
    @constraint(model, E6, - X1 + X12 - 0.15*Y9*Y10 - 0.1319*Y9*Y11 - 0.141421*Y10*Y11 == 0)
    @constraint(model, E7, - X6 + 67440*Y9 - 58316*X29 + 24556*X32 - 41*X35 - 5507*Y9*X32 + Y9*X35 + 667*X29*X32 == 28800)
    @constraint(model, E8, - X7 + 67440*Y10 - 58316*X30 + 24556*X33 - 41*X36 - 5507*Y10*X33 + Y10*X36 + 667*X30*X33 == 28800)
    @constraint(model, E9, - X8 + 67440*Y11 - 58316*X31 + 24556*X34 - 41*X37 - 5507*Y11*X34 + Y11*X37 + 667*X31*X34 == 28800)
    @constraint(model, E10, - X29 + SQR(Y9) == 0)
    @constraint(model, E11, - X32 + Y9*X29 == 0)
    @constraint(model, E12, - X35 + SQR(X32) == 0)
    @constraint(model, E13, - X30 + SQR(Y10) == 0)
    @constraint(model, E14, - X33 + Y10*X30 == 0)
    @constraint(model, E15, - X36 + SQR(X33) == 0)
    @constraint(model, E16, - X31 + SQR(Y11) == 0)
    @constraint(model, E17, - X34 + Y11*X31 == 0)
    @constraint(model, E18, - X37 + SQR(X34) == 0)
    @constraint(model, E19, objvar == X0)
    @objective(model, Min, objvar)
    optimize!(model)
    return model
end
