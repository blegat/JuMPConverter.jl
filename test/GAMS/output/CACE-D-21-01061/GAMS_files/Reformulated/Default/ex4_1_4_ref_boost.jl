using JuMP
model = Model()
@variable(model, objvar)
@variable(model, -167.876445958133 <= X0 <= 194.1573232142336)
@variable(model, 0 <= X2 <= 12.07683640645985)
@variable(model, 0 <= X1 <= 3.47517429871652)
@constraint(model, E1, - X0 + 4*X2 - 4*X1*X2 + SQR(X2) == 0)
@constraint(model, E2, - X2 + SQR(X1) == 0)
@constraint(model, E3, objvar == X0)
@objective(model, Min, objvar)
optimize!(model)
