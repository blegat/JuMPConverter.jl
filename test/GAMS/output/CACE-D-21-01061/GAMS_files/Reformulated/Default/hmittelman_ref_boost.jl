using JuMP
function build_model()
    model = Model()
    @variable(model, objvar)
    @variable(model, 0 <= X0 <= 50)
    @variable(model, 0 <= X36 <= 1)
    @variable(model, 0 <= X24 <= 1)
    @variable(model, 0 <= X31 <= 1)
    @variable(model, 0 <= X27 <= 1)
    @variable(model, 0 <= X38 <= 1)
    @variable(model, 1 <= X28 <= 1)
    @variable(model, 0 <= X29 <= 1)
    @variable(model, 0 <= X35 <= 1)
    @variable(model, 0 <= X30 <= 1)
    @variable(model, 0 <= X33 <= 1)
    @variable(model, 0 <= X37 <= 1)
    @variable(model, 0 <= X34 <= 1)
    @variable(model, 0 <= X39 <= 1)
    @variable(model, -25 <= X1 <= 0)
    @variable(model, -14 <= X2 <= 0)
    @variable(model, -5 <= X3 <= 0)
    @variable(model, -7 <= X4 <= 0)
    @variable(model, -19 <= X5 <= 0)
    @variable(model, -27 <= X6 <= 0)
    @variable(model, -30 <= X7 <= 0)
    @variable(model, 0 <= X26 <= 1)
    @variable(model, 0 <= X32 <= 1)
    @variable(model, 0 <= X25 <= 1)
    @variable(model, Y10, Bin)
    @variable(model, Y20, Bin)
    @variable(model, Y12, Bin)
    @variable(model, Y14, Bin)
    @variable(model, Y16, Bin)
    @variable(model, Y17, Bin)
    @variable(model, Y21, Bin)
    @variable(model, Y22, Bin)
    @variable(model, Y23, Bin)
    @variable(model, Y8, Bin)
    @variable(model, Y9, Bin)
    @variable(model, Y11, Bin)
    @variable(model, Y15, Bin)
    @variable(model, Y18, Bin)
    @variable(model, Y13, Bin)
    @variable(model, Y19, Bin)
    @constraint(model, E1, - X0 + 12*Y10*X36 + 3*X24*X31 + 5*X27*X38 + 8*X28*X29 + X28*X35 + X30*X33 + 3*X33*X37 + 7*X34*X36 + 10*X38*X39 == 0)
    @constraint(model, E2, - X1 + Y10*X36 + 2*Y20*X38 - 7*X24*X31 - 8*X28*X35 - 12*X34*X36 + 3*X38*X39 == -2)
    @constraint(model, E3, - X2 + X27*X38 - 5*X28*X29 - 10*X28*X35 + 7*X30*X33 + X33*X37 + X34*X36 == -1)
    @constraint(model, E4, - X3 + Y20*X38 - 2*X27*X38 - X28*X35 - 3*X34*X36 + 5*X38*X39 == -1)
    @constraint(model, E5, - X4 - Y20*X38 + 2*X27*X38 + X28*X35 + 3*X34*X36 - 5*X38*X39 == 1)
    @constraint(model, E6, - X5 - 2*Y10*X36 - 2*X24*X31 - 9*X27*X38 - 4*X28*X35 + X30*X33 - 5*X33*X37 == -3)
    @constraint(model, E7, - X6 - 12*Y10*X36 + 3*Y20*X38 - 15*X24*X31 + 2*X27*X38 - 7*X28*X29 + 6*X33*X37 + 9*X34*X36 == -7)
    @constraint(model, E8, - X7 - 7*Y10*X36 - 10*X24*X31 - X28*X29 + 2*X28*X35 - 5*X30*X33 + 5*X34*X36 - 8*X38*X39 == -1)
    @constraint(model, E9, - X26 + Y12*Y14 == 0)
    @constraint(model, E10, - X30 + Y16*Y17 == 0)
    @constraint(model, E11, - X39 + X26*X30 == 0)
    @constraint(model, E12, - X32 + Y21*Y22 == 0)
    @constraint(model, E13, - X38 + Y23*X32 == 0)
    @constraint(model, E14, - X24 + Y8*Y9 == 0)
    @constraint(model, E15, - X34 + Y10*X24 == 0)
    @constraint(model, E16, - X25 + Y11*Y15 == 0)
    @constraint(model, E17, - X36 + Y18*X25 == 0)
    @constraint(model, E18, - X28 + Y13*Y14 == 0)
    @constraint(model, E19, - X37 + Y16*X28 == 0)
    @constraint(model, E20, - X33 + Y21*Y23 == 0)
    @constraint(model, E21, - X27 + Y12*Y17 == 0)
    @constraint(model, E22, - X35 + Y10*X25 == 0)
    @constraint(model, E23, - X29 + Y15*Y19 == 0)
    @constraint(model, E24, - X31 + Y18*Y19 == 0)
    @constraint(model, E25, objvar == X0)
    @objective(model, Min, objvar)
    return model
end
