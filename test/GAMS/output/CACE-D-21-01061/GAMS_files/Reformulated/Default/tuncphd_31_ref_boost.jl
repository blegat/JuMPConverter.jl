using JuMP
function build_model()
    model = Model()
    @variable(model, objvar)
    @variable(model, -18 <= X0 <= 3.485798348638073)
    @variable(model, 0 <= X6 <= 1.333333333333333)
    @variable(model, 0 <= X8 <= 1)
    @variable(model, 0 <= X9 <= 2)
    @variable(model, 0 <= X10 <= 1.188401638644003)
    @variable(model, 0 <= X11 <= 2.297396709994071)
    @variable(model, 0 <= X1 <= 0)
    @variable(model, 0 <= X7 <= 4)
    @variable(model, -4 <= X2 <= -0.6666666666666665)
    @variable(model, -4 <= X3 <= 0)
    @variable(model, 0 <= X4 <= 0)
    @variable(model, 0 <= X25 <= 1.777777777777778)
    @variable(model, 0 <= X29 <= 1.994586925237691)
    @variable(model, 0 <= X5 <= 0)
    @variable(model, 0 <= X26 <= 16)
    @variable(model, 0 <= X30 <= 27.85761802547601)
    @variable(model, 0 <= X27 <= 1.412298454731751)
    @variable(model, 0 <= X28 <= 5.27803164309158)
    @constraint(model, E1, - X0 - 6*X6 - 4*X8 - 3*X9 + X10 + X11 == -0)
    @constraint(model, E2, - X1 - 3*X6 + X7 - 3*X8 == -0)
    @constraint(model, E3, - X2 + X6 + 2*X8 == 4)
    @constraint(model, E4, - X3 + X7 + 2*X9 == 4)
    @constraint(model, E5, - X4 + X6*X25 - X10*X29 == 0)
    @constraint(model, E6, - X5 + X7*X26 - X11*X30 == 0)
    @constraint(model, E7, - X27 + SQR(X10) == 0)
    @constraint(model, E8, - X29 + SQR(X27) == 0)
    @constraint(model, E9, - X25 + SQR(X6) == 0)
    @constraint(model, E10, - X28 + SQR(X11) == 0)
    @constraint(model, E11, - X30 + SQR(X28) == 0)
    @constraint(model, E12, - X26 + SQR(X7) == 0)
    @constraint(model, E13, objvar == X0)
    @objective(model, Min, objvar)
    return model
end
