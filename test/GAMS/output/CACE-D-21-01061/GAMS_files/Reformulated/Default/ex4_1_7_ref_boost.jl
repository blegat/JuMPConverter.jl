using JuMP
model = Model()
@variable(model, objvar)
@variable(model, -117.4569775789868 <= X0 <= 131.630896558601)
@variable(model, -1.491809558595276 <= X1 <= 3.086907286808442)
@variable(model, 0 <= X2 <= 9.528996597351057)
@constraint(model, E1, - X0 + 10*X1 - 1.5*X2 - 3*X1*X2 + SQR(X2) == 0)
@constraint(model, E2, - X2 + SQR(X1) == 0)
@constraint(model, E3, objvar == X0)
@objective(model, Min, objvar)
optimize!(model)
