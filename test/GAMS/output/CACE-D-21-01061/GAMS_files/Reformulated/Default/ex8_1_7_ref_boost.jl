using JuMP
function build_model()
    model = Model()
    @variable(model, objvar)
    @variable(model, -260.0867874943356 <= X0 <= 14469.76610410233)
    @variable(model, -5 <= X6 <= 5)
    @variable(model, 0 <= X11 <= 25)
    @variable(model, -4.17157287525381 <= X7 <= 5)
    @variable(model, 0 <= X12 <= 8.264161152665611)
    @variable(model, -2.874745406582226 <= X8 <= 2.211641500445154)
    @variable(model, -14.37372703291113 <= X13 <= 11.05820750222577)
    @variable(model, 0 <= X14 <= 25)
    @variable(model, -25 <= X15 <= 25)
    @variable(model, 0 <= X16 <= 25)
    @variable(model, -35.00000000000001 <= X1 <= 0)
    @variable(model, -34.57528993896852 <= X2 <= 0)
    @variable(model, -16.75225513765566 <= X3 <= 0)
    @variable(model, -3.488093984990042 <= X9 <= 5)
    @variable(model, -9.17157287525381 <= X4 <= 0)
    @variable(model, -0 <= X5 <= 0)
    @variable(model, -5 <= X10 <= 5)
    @constraint(model, E1, - X0 - 2*X6 + X11 + 2*SQR(X6) - 2*X6*X7 + X7*X11 + 3*X7*X12 - 3*X8*X11 - X8*X12 + SQR(X12) - 4*X12*X13 + 6*SQR(X13) - 4*X13*X14 + 2*SQR(X14) - 4*X14*X15 + 6*SQR(X15) - 4*X15*X16 + SQR(X16) == -1)
    @constraint(model, E2, - X1 + X6 + X11 + X8*X12 == 6.24264)
    @constraint(model, E3, - X2 - X6 - X11 - X8*X12 == -6.24264)
    @constraint(model, E4, - X3 + X7 + X9 - X12 == 0.828427)
    @constraint(model, E5, - X4 - X7 - X9 + X12 == -0.828427)
    @constraint(model, E6, - X5 + X6*X10 == 2)
    @constraint(model, E7, - X16 + SQR(X10) == 0)
    @constraint(model, E8, - X15 + X9*X10 == 0)
    @constraint(model, E9, - X14 + SQR(X9) == 0)
    @constraint(model, E10, - X13 + X8*X9 == 0)
    @constraint(model, E11, - X12 + SQR(X8) == 0)
    @constraint(model, E12, - X11 + SQR(X7) == 0)
    @constraint(model, E13, objvar == X0)
    @objective(model, Min, objvar)
    return model
end
