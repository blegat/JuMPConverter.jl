using JuMP
function build_model()
    model = Model()
    @variable(model, objvar)
    @variable(model, -0.1044259885765495 <= X1 <= 0)
    @variable(model, 56.11782442608552 <= X6 <= 61.52849740932643)
    @variable(model, -0.08963595497514421 <= X2 <= -0.0380181347150258)
    @variable(model, 6074.832126530112 <= X0 <= 8641.485954024769)
    @variable(model, 1010.120839669539 <= X13 <= 1169.041450777202)
    @variable(model, 3149.21021831696 <= X14 <= 3785.755993449489)
    @variable(model, 26.93088763846479 <= X7 <= 54.33074194793367)
    @variable(model, 324 <= X12 <= 361)
    @variable(model, -0 <= X3 <= 325874.7802265713)
    @variable(model, 18 <= Y4 <= 19, Int)
    @variable(model, 10 <= Y5 <= 10, Int)
    @constraint(model, E1, - X1 + 0.0193*X6 - 0.0625*Y4 == -0)
    @constraint(model, E2, - X2 + 0.00954*X6 - 0.0625*Y5 == -0)
    @constraint(model, E3, - X0 + 0.0775*Y4*X13 + 0.111131*Y5*X14 + 0.012348*X7*X12 + 0.0389*X7*X13 == 0)
    @constraint(model, E4, - X3 + 4.18879*X6*X14 + 3.14159*X7*X14 == 1.296e+06)
    @constraint(model, E5, - X14 + SQR(X6) == 0)
    @constraint(model, E6, - X13 + Y4*X6 == 0)
    @constraint(model, E7, - X12 + SQR(Y4) == 0)
    @constraint(model, E8, objvar == X0)
    @objective(model, Min, objvar)
    optimize!(model)
    return model
end
