using JuMP
function build_model()
    model = Model()
    @variable(model, objvar)
    @variable(model, -263.7522000000001 <= X0 <= -0)
    @variable(model, 0 <= X4 <= 5)
    @variable(model, 0 <= X8 <= 26244000000)
    @variable(model, -675 <= X1 <= 0)
    @variable(model, 0 <= X6 <= 1296)
    @variable(model, -4190000 <= X2 <= 0)
    @variable(model, 0 <= X5 <= 125)
    @variable(model, 0 <= X7 <= 162000)
    @variable(model, 0 <= X3 <= 36)
    @constraint(model, E1, - X0 - 2.01e-09*X4*X8 == 0)
    @constraint(model, E2, - X1 + X4*X6 == 675)
    @constraint(model, E3, - X2 + X5*X7 == 4.19e+06)
    @constraint(model, E4, - X6 + SQR(X3) == 0)
    @constraint(model, E5, - X7 + X5*X6 == 0)
    @constraint(model, E6, - X8 + SQR(X7) == 0)
    @constraint(model, E7, objvar == X0)
    @objective(model, Min, objvar)
    optimize!(model)
    return model
end
